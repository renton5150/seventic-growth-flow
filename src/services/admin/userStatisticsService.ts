
import { supabase } from "@/integrations/supabase/client";

export interface UserWithStats {
  id: string;
  name: string;
  email: string;
  role: 'sdr' | 'growth' | 'admin';
  avatar?: string;
  stats: {
    total: number;
    pending: number;
    completed: number;
    late: number;
    unassigned?: number; // Pour Growth uniquement
  };
}

export interface GlobalStatistics {
  totalUsers: number;
  totalPending: number;
  totalCompleted: number;
  totalLate: number;
}

/**
 * Service pour r√©cup√©rer les statistiques globales - VERSION CORRIG√âE
 */
export const fetchGlobalStatistics = async (): Promise<GlobalStatistics> => {
  try {
    console.log("[GlobalStatistics] üöÄ R√©cup√©ration des statistiques globales - VERSION CORRIG√âE");
    
    // 1. R√©cup√©rer tous les utilisateurs actifs
    const { data: users, error: usersError } = await supabase
      .from('profiles')
      .select('id, role')
      .in('role', ['sdr', 'growth', 'admin']);

    if (usersError) {
      console.error("[GlobalStatistics] ‚ùå Erreur users:", usersError);
      throw new Error(`Erreur r√©cup√©ration utilisateurs: ${usersError.message}`);
    }

    // 2. R√©cup√©rer toutes les demandes ACTIVES (non compl√©t√©es, non annul√©es)
    const { data: allRequests, error: requestsError } = await supabase
      .from('requests_with_missions')
      .select('workflow_status, due_date, assigned_to, created_by')
      .not('workflow_status', 'in', '(completed,canceled)'); // Exclure les compl√©t√©es et annul√©es

    if (requestsError) {
      console.error("[GlobalStatistics] ‚ùå Erreur requests:", requestsError);
      throw new Error(`Erreur r√©cup√©ration demandes: ${requestsError.message}`);
    }

    console.log(`[GlobalStatistics] üìä ${allRequests?.length || 0} demandes actives trouv√©es`);

    // 3. Calculer les statistiques globales bas√©es sur les demandes ACTIVES uniquement
    const totalUsers = users?.length || 0;
    
    // Demandes en attente = pending_assignment OU in_progress
    const totalPending = allRequests?.filter(req => 
      req.workflow_status === 'pending_assignment' || 
      req.workflow_status === 'in_progress'
    ).length || 0;
    
    // Demandes termin√©es = celles avec workflow_status 'completed' (mais on les a exclues de la requ√™te)
    // Donc on va les r√©cup√©rer s√©par√©ment
    const { data: completedRequests } = await supabase
      .from('requests_with_missions')
      .select('id')
      .eq('workflow_status', 'completed');
    
    const totalCompleted = completedRequests?.length || 0;
    
    // Demandes en retard = demandes actives avec due_date d√©pass√©e
    const totalLate = allRequests?.filter(req => 
      req.due_date && 
      new Date(req.due_date) < new Date()
    ).length || 0;

    const globalStats: GlobalStatistics = {
      totalUsers,
      totalPending,
      totalCompleted,
      totalLate
    };

    console.log("[GlobalStatistics] ‚úÖ Statistiques globales calcul√©es (VERSION CORRIG√âE):", globalStats);
    
    return globalStats;
  } catch (error) {
    console.error("[GlobalStatistics] üí• Erreur:", error);
    throw error;
  }
};

/**
 * Service pour r√©cup√©rer les statistiques des utilisateurs - VERSION ULTRA CORRIG√âE
 */
export const fetchUserStatistics = async (): Promise<UserWithStats[]> => {
  try {
    console.log("[UserStatisticsService] üöÄ D√âMARRAGE ULTRA CORRIG√â - R√©cup√©ration des statistiques utilisateur");
    
    // 1. R√©cup√©rer tous les utilisateurs SDR et Growth
    const { data: users, error: usersError } = await supabase
      .from('profiles')
      .select('id, name, email, role, avatar')
      .in('role', ['sdr', 'growth']);

    if (usersError) {
      console.error("[UserStatisticsService] ‚ùå Erreur users:", usersError);
      throw new Error(`Erreur r√©cup√©ration utilisateurs: ${usersError.message}`);
    }

    if (!users || users.length === 0) {
      console.warn("[UserStatisticsService] ‚ö†Ô∏è Aucun utilisateur trouv√©");
      return [];
    }

    console.log(`[UserStatisticsService] üë• ${users.length} utilisateurs r√©cup√©r√©s:`, users.map(u => ({ name: u.name, role: u.role })));

    // 2. R√©cup√©rer TOUTES les demandes avec les d√©tails
    const { data: allRequests, error: requestsError } = await supabase
      .from('requests_with_missions')
      .select('*');

    if (requestsError) {
      console.error("[UserStatisticsService] ‚ùå Erreur requests:", requestsError);
      throw new Error(`Erreur r√©cup√©ration demandes: ${requestsError.message}`);
    }

    console.log(`[UserStatisticsService] üìã ${allRequests?.length || 0} demandes r√©cup√©r√©es au total`);

    // 3. CALCUL CORRIG√â : Demandes non assign√©es (pour Growth)
    const unassignedRequests = allRequests?.filter(req => 
      !req.assigned_to && 
      req.workflow_status !== 'completed' && 
      req.workflow_status !== 'canceled'
    ) || [];
    
    console.log(`[UserStatisticsService] üîç ULTRA CORRIG√â - Demandes non assign√©es: ${unassignedRequests.length}`);

    // 4. Calculer les stats pour chaque utilisateur - LOGIQUE ULTRA CORRIG√âE
    const usersWithStats: UserWithStats[] = users.map(user => {
      let userRequests;
      let stats;

      if (user.role === 'sdr') {
        // Pour SDR : demandes cr√©√©es par eux (TOUTES, y compris compl√©t√©es)
        userRequests = allRequests?.filter(req => req.created_by === user.id) || [];
        
        // Calcul CORRIG√â pour SDR
        stats = {
          total: userRequests.length,
          // En attente = demandes cr√©√©es par le SDR qui sont pending_assignment OU in_progress
          pending: userRequests.filter(req => 
            req.workflow_status === 'pending_assignment' || 
            req.workflow_status === 'in_progress'
          ).length,
          completed: userRequests.filter(req => req.workflow_status === 'completed').length,
          // En retard = demandes cr√©√©es par le SDR qui ne sont pas compl√©t√©es ET ont due_date d√©pass√©e
          late: userRequests.filter(req => 
            req.workflow_status !== 'completed' && 
            req.workflow_status !== 'canceled' &&
            req.due_date && 
            new Date(req.due_date) < new Date()
          ).length
        };
        
        console.log(`[UserStatisticsService] üìä SDR ${user.name} - ULTRA CORRIG√â:`, {
          total: stats.total,
          pending: stats.pending,
          completed: stats.completed,
          late: stats.late,
          'pending_requests_details': userRequests.filter(req => 
            req.workflow_status === 'pending_assignment' || 
            req.workflow_status === 'in_progress'
          ).map(req => ({ id: req.id, title: req.title, workflow_status: req.workflow_status }))
        });
        
      } else {
        // Pour Growth : demandes assign√©es √† eux
        userRequests = allRequests?.filter(req => req.assigned_to === user.id) || [];
        
        // Calcul CORRIG√â pour Growth
        stats = {
          total: userRequests.length,
          // En attente = demandes assign√©es au Growth qui sont pending_assignment OU in_progress
          pending: userRequests.filter(req => 
            req.workflow_status === 'pending_assignment' || 
            req.workflow_status === 'in_progress'
          ).length,
          completed: userRequests.filter(req => req.workflow_status === 'completed').length,
          // En retard = demandes assign√©es au Growth qui ne sont pas compl√©t√©es ET ont due_date d√©pass√©e
          late: userRequests.filter(req => 
            req.workflow_status !== 'completed' && 
            req.workflow_status !== 'canceled' &&
            req.due_date && 
            new Date(req.due_date) < new Date()
          ).length,
          unassigned: unassignedRequests.length // M√äME NOMBRE pour tous les Growth
        };
        
        console.log(`[UserStatisticsService] üìä Growth ${user.name} - ULTRA CORRIG√â:`, {
          total: stats.total,
          pending: stats.pending,
          completed: stats.completed,
          late: stats.late,
          unassigned: stats.unassigned,
          'pending_requests_details': userRequests.filter(req => 
            req.workflow_status === 'pending_assignment' || 
            req.workflow_status === 'in_progress'
          ).map(req => ({ id: req.id, title: req.title, workflow_status: req.workflow_status }))
        });
      }

      return {
        id: user.id,
        name: user.name,
        email: user.email,
        role: user.role as 'sdr' | 'growth' | 'admin',
        avatar: user.avatar,
        stats
      };
    });

    console.log("[UserStatisticsService] ‚úÖ ULTRA CORRIG√â - Statistiques calcul√©es pour tous les utilisateurs");
    
    return usersWithStats;
  } catch (error) {
    console.error("[UserStatisticsService] üí• Erreur finale:", error);
    throw error;
  }
};

